#!/bin/bash
echo "================================================================"
echo " SYSTEM SPECS                                                   "
echo " Generated: $(date)                                             "
echo "================================================================"
echo "++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++"
uptime                # System uptime
echo "================================================================"
whoami                # Current user
echo "================================================================"
who                   # Logged in users
echo "================================================================"
uname -a              # System information
echo "================================================================"
hostname              # Computer name
echo "================================================================"
lscpu                 # CPU information
echo "================================================================"
lsmem                 # Memory information
echo "================================================================"
lsblk                 # Block devices (disks)
echo "================================================================"
lsusb                 # USB devices
echo "================================================================"
lspci                 # PCI devices
echo "================================================================"
free -h               # Memory usage
echo "================================================================"
df -h                 # Disk space usage
echo "================================================================"
